package aula;

class Calculadora {

    int somar(int x, int y){
        int soma = x + y;
        return soma;
    }

    int subtrair (int x, int y){
        int subtrai = x - y;
        return subtrai;
    }

    int multiplicar (int x, int y){
        int multiplica = x * y;
        return multiplica;
    }

    double dividir(double x, double y){
        double divide = x/y;
        return divide;
    }
}